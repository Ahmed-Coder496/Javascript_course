// Data types of javascript summary

/*
Data types are divided in 2 catagories

1. Primitive

2. Non-Primitive / Referance
*/

/*
// PRIMITIVE

There are 7 types of primitive
*/

// String
console.log(typeof"Ahmed");

// Boolean
console.log(typeof true);

// Number
console.log(typeof 100);

// null
console.log(typeof null);

// undefined
console.log(typeof undefined);

// BigInt
console.log(typeof 2467123121564n);

// Symbol
let id = Symbol(145)
let idPassword = Symbol(145)
console.log(typeof id);
console.log(typeof idPassword);
console.log(id == idPassword);


